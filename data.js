/* ---------------- LOAD CITIES ---------------- */
function loadCities() {
  const state = document.getElementById("state").value;
  const city = document.getElementById("city");

  console.log("Selected state:", state);
  console.log("Data object:", data);
  console.log("State data:", data ? data[state] : "data missing");

  city.innerHTML = `<option value="">Select City</option>`;

  if (!data || !data[state]) {
    alert("State data NOT FOUND. Check console.");
    return;
  }

  Object.keys(data[state]).forEach(c => {
    const opt = document.createElement("option");
    opt.value = c;
    opt.textContent = c;
    city.appendChild(opt);
  });
}


/* ---------------- LOAD SERVICES ---------------- */
function loadServices() {
  const state = document.getElementById("state").value;
  const city = document.getElementById("city").value;

  let buttons = "";
  document.getElementById("cards").innerHTML = "";

  if (!state || !city) return;

  for (let service in data[state][city]) {
    buttons += `<button onclick="showService('${service}')">${service}</button>`;
  }

  document.getElementById("serviceButtons").innerHTML = buttons;
}

/* ---------------- SHOW SERVICE DATA ---------------- */
function showService(service) {
  const state = document.getElementById("state").value;
  const city = document.getElementById("city").value;

  let html = "";

  data[state][city][service].forEach(item => {
    html += `
      <div class="card">
        <p><strong>Name:</strong> ${item.name}</p>
        ${item.address ? `<p><strong>Address:</strong> ${item.address}</p>` : ""}
        ${item.phone ? `<p><strong>Phone:</strong> ${item.phone}</p>` : ""}
        ${item.link ? `<a href="${item.link}" target="_blank">View Map</a>` : ""}
      </div>
    `;
  });

  document.getElementById("cards").innerHTML = html;
}
